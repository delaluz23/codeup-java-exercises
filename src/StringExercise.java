public class StringExercise {
    public static void main(String[] args) {
        String one = "We don't need no education";
        String two = "We don't need no thought control";
        System.out.println(one);
        System.out.println(two);
        String three = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(three);
        String four = "In windows, the main drive is usually C:\\";
        System.out.println(four);
        String five = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(five);
    }
}
